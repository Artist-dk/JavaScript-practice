/* Assignment Operators
1. = -> Simple assignment
2. += -> Add and assignment
3. -+ -> Subtract and assignment
4. *= -> Multiply and assignemnt
5. /= -> Divide and assignment
6. %= -> Modules and assignment
*/

var a = 33;
var b = 10;

console.log(`L1 : a = b -> ${a = b}`)
console.log(`L2 : a += b -> ${a += b}`)
console.log(`L3 : a -= b -> ${a -= b}`)
console.log(`L4 : a *= b -> ${a *= b}`)
console.log(`L5 : a /= b -> ${a /= b}`)
console.log(`L6 : a %= b -> ${a %= b}`)