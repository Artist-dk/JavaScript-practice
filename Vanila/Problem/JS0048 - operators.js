/* Bitwise Operators
1. & -> Bitwise AND
2. | -> Bitwise OR
3. ^ -> Bitwise XOR
4. ~ -> Bitwise NOT
5. << -> Left shift 
6. >> -> Right shift
7. >>> -> Right shift with zero
*/



var a = 2 // Bit presentation 10
var b = 3 // Bit presentation 11

console.log(`L1 : a & b -> ${a & b}`)
console.log(`L2 : a | b -> ${a | b}`)
console.log(`L3 : a ^ b -> ${a ^ b}`)
console.log(`L4 : ~a -> ${~a}`)
console.log(`L5 : a << b -> ${a << b}`)
console.log(`L6 : a >> b -> ${a >> b}`)
console.log(`L7 : a >>> b -> ${a >>> b}`)